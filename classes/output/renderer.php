namespace local_aihub\output;

use plugin_renderer_base;

/**
 * Renders local_aihub pages from Mustache templates.
 *
 * @package    local_aihub
 * @copyright  2026 Jean Lúcio
 * @license    https://www.gnu.org/copyleft/gpl.html GNU GPL v3 or later
 */
class renderer extends plugin_renderer_base {
    /**
     * Renders the "My AI keys" page.
     *
     * @param mykeys $page The page renderable.
     * @return string HTML.
     */
    public function render_mykeys(mykeys $page): string {
        return $this->render_from_template('local_aihub/mykeys', $page->export_for_template($this));
    }
}
